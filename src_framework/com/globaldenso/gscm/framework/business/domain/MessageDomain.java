package com.globaldenso.gscm.framework.business.domain;

/**
 * メッセージDomainクラス
 * <P>
 * メッセージのDomainクラスです。
 * </P>
 * 
 * @author $Author: 10088DS01586 $
 */

public final class MessageDomain {

    /**
     * PROPERTY
     */
    private String property = null;

    /**
     * KEY
     */
    private String key = null;

    /**
     * PARAMS
     */
    private Object[] params = null;

    /**
     * デフォルトコンストラクタです。
     */
    public MessageDomain() {
        super();
    }

    /**
     * デフォルトコンストラクタです。
     * 
     * @param property PROPERTY
     * @param key KEY
     * @param params PARAMS
     */
    public MessageDomain(String property, String key, Object[] params) {
        this.property = property;
        this.key = key;
        this.params = params;
    }

    /**
     * property のゲッターメソッドです。
     * 
     * @return property
     */
    public String getProperty() {
        return property;
    }

    /**
     * property のセッターメソッドです。
     * 
     * @param property property に設定する
     */
    public void setProperty(String property) {
        this.property = property;
    }

    /**
     * key のゲッターメソッドです。
     * 
     * @return the key
     */
    public String getKey() {
        return key;
    }

    /**
     * key のセッターメソッドです。
     * 
     * @param key key に設定する
     */
    public void setKey(String key) {
        this.key = key;
    }

    /**
     * params のゲッターメソッドです。
     * 
     * @return the params
     */
    public Object[] getParams() {
        return params;
    }

    /**
     * params のセッターメソッドです。
     * 
     * @param params params に設定する
     */
    public void setParams(Object[] params) {
        this.params = params;
    }
}
