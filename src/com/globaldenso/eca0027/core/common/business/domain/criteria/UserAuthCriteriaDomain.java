package com.globaldenso.eca0027.core.common.business.domain.criteria;

import com.globaldenso.gscm.framework.business.domain.AbstractDomain;

/**
 * The criteria domain class to get the use authority of user's function <br />
 * ユーザ機能利用権限を取得するためのCriteriaドメインです。
 *
 * @author $Author: 10088DS01586 $
 * @version $Revision: 5759 $
 */
public class UserAuthCriteriaDomain extends AbstractDomain {

    /**
     * serial version <br />
     * シリアルバージョン
     */
    private static final long serialVersionUID = 1L;

    /**
     * DSC-ID
     */
    private String dscId;
    
    /**
     * SERVER ID (ID of a server which the user logs in to) <br />
     * サーバID (ユーザがログインしているサーバのID)
     */
    private String serverId;
    
    /**
     * constructor. <br>
     * デフォルトコンストラクタです。
     */
    public UserAuthCriteriaDomain() {
    }

    /**
     * Getter method for dscId.
     *
     * @return the dscId
     */
    public String getDscId() {
        return dscId;
    }

    /**
     * Setter method for dscId.
     *
     * @param dscId Set for dscId
     */
    public void setDscId(String dscId) {
        this.dscId = dscId;
    }

    /**
     * Getter method for serverId.
     *
     * @return the serverId
     */
    public String getServerId() {
        return serverId;
    }

    /**
     * Setter method for serverId.
     *
     * @param serverId Set for serverId
     */
    public void setServerId(String serverId) {
        this.serverId = serverId;
    }

}
